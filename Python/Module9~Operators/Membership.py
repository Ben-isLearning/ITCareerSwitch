
alphabet = ["a", "b", "c"]

print("a" in alphabet)
# is True ~ "a" does exist in alphabet

print(123 in alphabet)
# is False ~ 123 does not exist in alphabet.

print("--------")

print("a" not in alphabet)
# is False ~ "a" does exist in alphabet

print(123 not in alphabet)
# is True ~ 123 does not exist in alphabet.


# in          True if value is present in the object          a   in alphabet
# not in      True if value is not present in the object      123 in alphabet
