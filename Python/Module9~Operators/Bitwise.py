# Preface

alpha = 101

beta = 42

print(bin(alpha))
print(bin(beta))

# In Python, binary starts with "0b" - then the number
# Binary consists of 8 digits
# alpha = 01100101  (0b1100101)
# beta  = 00101010  (0b101010)

print(alpha & beta)
print(bin(32))

print(alpha | beta)
print(bin(111))

print(alpha ^ beta)
print(bin(79))
